import turtle
turtle.Screen().bgcolor("Blue")
turtle.Screen().setup(300,400)
t=turtle.Turtle()
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)
t.forward(100)
t.right(150)
t.penup()
t.forward(50)
t.pendown()
t.right(90)
t.forward(100)
t.right(120)
t.forward(100)
t.right(120)
t.forward(100)

turtle.done()